

public class SumOfEvenNumber {

	public static void main(String[] args) {
		
		        int count = 0;  
		        int sum = 0; 
		        
		        for (int i = 2; count < 1000; i += 2)
		        {
		            sum += i;
		            count++;
		        }
		        
		        System.out.println("Sum of the first 1000 even numbers: " + sum);
		    }
	


	}

